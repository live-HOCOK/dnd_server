package parse;

public class Race  {
    public String raceName;
    public int health;
    public int armor;
    public int force;
    public int agility;
    public int intelligence;
    public int charisma;

    public int getCharisma() {
        return charisma;
    }

    public int getIntelligence() {
        return intelligence;
    }

    public int getForce() {
        return force;
    }

    public int getAgility() {
        return agility;
    }

    public int getHealth() {
        return health;
    }

    public int getArmor() {
        return armor;
    }

    public String getRaceName() {
        return raceName;
    }
}
